/// 4) ตัวช่วย: ค่าหลัง apply patch (อ่านอย่างเดียว)
/// ----------------------------------------------------------------------------
Map<String, Map<String, Object?>> get mergedShelfLifeSubcategoryData {
  final Map<String, Map<String, Object?>> out = {
    for (final e in shelfLifeSubcategoryData.entries) e.key: Map.from(e.value),
  };
  for (final entry in shelfLifePatch.entries) {
    final sub = entry.key;
    final patch = entry.value;
    if (!out.containsKey(sub)) continue;
    final current = Map<String, Object?>.from(out[sub]!);
    final storage = Map<String, List<int>>.from(
      current['storage'] as Map<String, List<int>>,
    );
    for (final kv in patch.entries) {
      storage[kv.key] = kv.value;
    }
    current['storage'] = storage;
    out[sub] = current;
  }
  return out;
}
